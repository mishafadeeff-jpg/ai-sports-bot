def is_valid_sports_query(query: str) -> bool:
    """Проверяет, содержит ли запрос пользователя реальные названия команд."""
    query_lower = query.lower().strip()
    
    # Слишком короткий запрос или без разделителей
    if len(query_lower) < 4:
        return False
        
    # Ищем хотя бы одно совпадение с известными командами или ключевыми спортивными терминами
    for team in KNOWN_TEAMS:
        if team in query_lower:
            return True
            
    # Если введено 2 слова разделенные дефисом/vs длиной от 3 букв каждое, проверяем на адекватность
    parts = query_lower.replace("-", " vs ").replace("—", " vs ").replace("против", " vs ").split(" vs ")
    if len(parts) >= 2:
        p1, p2 = parts[0].strip(), parts[1].strip()
        # Отсекаем явный бред (случайные наборы согласных или абракадабру)
        if len(p1) >= 3 and len(p2) >= 3 and not (p1.isdigit() or p2.isdigit()):
            # Простая проверка на гласные буквы в словах
            vowels = "аеёиоуыэюяaeiouy"
            if any(char in vowels for char in p1) and any(char in vowels for char in p2):
                return True

    return False

async def analyze_match(match_query: str) -> str:
    """
    Основная функция разбора матча.
    Проверяет валидность и генерирует прогноз.
    """
    # 1. Проверяем валидность запроса
    if not is_valid_sports_query(match_query):
        return (
            "⚠️ **МАТЧ НЕ НАЙДЕН В БАЗЕ ДАННЫХ!**\n\n"
            "ИИ-аналитик не нашел указанные команды в официальных спортивных реестрах (Футбол, Хоккей, Баскетбол).\n\n"
            "📌 **Пожалуйста, укажите реальные команды:**\n"
            "• `Реал Мадрид - Барселона`\n"
            "• `СКА vs ЦСКА`\n"
            "• `Манчестер Сити - Ливерпуль`\n"
            "• `Лейкерс - Бостон`"
        )

    if config.GEMINI_API_KEY:
        try:
            import google.generativeai as genai
            genai.configure(api_key=config.GEMINI_API_KEY)
            model = genai.GenerativeModel('gemini-pro')
            
            prompt = (
                f"Ты — профессиональный ИИ-аналитик спортивных ставок. Проанализируй следующий матч: '{match_query}'. "
                f"Если такого матча или команд не существует в реальном спорте, ответь только строкой: 'MATCH_NOT_FOUND'. "
                f"Иначе составь подробный прогноз с вероятностями побед в %, ключевыми аргументами, "
                f"рекомендуемой ставкой, средним коэффициентом и рекомендуемым процентом депозита (3-5%)."
            )
            response = await model.generate_content_async(prompt)
            if response.text:
                if "MATCH_NOT_FOUND" in response.text:
                    return (
                        "⚠️ **МАТЧ НЕ НАЙДЕН В БАЗЕ ДАННЫХ!**\n\n"
                        "ИИ-аналитик не нашел указанные команды в реестрах лиг. Пожалуйста, укажите реальные спортивные команды."
                    )
                return response.text
        except Exception as e:
            print(f"[WARN] Ошибка Gemini API: {e}. Переключаемся на локальную аналитику.")
            
    # Локальная аналитика валидного матча
    parts = match_query.replace("-", " vs ").replace("—", " vs ").replace("против", " vs ").split(" vs ")
    if len(parts) >= 2:
        t1, t2 = parts[0].strip().capitalize(), parts[1].strip().capitalize()
    else:
        t1 = match_query.strip().capitalize()
        t2 = "Соперник"
        
    return generate_local_analysis(t1, t2)

